        void mostrarproveedor() {
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("select * from proveedor");
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                cmb_proveedor.addItem(rs.getString("nombre_prov"));
            }

        } catch (SQLException e) {
            System.err.println("Error en cargar proveedores." + e);
            JOptionPane.showMessageDialog(null, "¬°¬°ERROR proveedores!!, contacte al administrador.");
        }
    }
