    private void jButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActionPerformed

        int validacion = 0, tipo_cmb, proveedor_cmb;
        String nombre, costo, precio, tipo_string = "", stock;

        costo = txt_costo.getText().trim();
        nombre = txt_nombre.getText().trim();
        precio = txt_precio.getText().trim();
        proveedor_cmb = cmb_proveedor.getSelectedIndex();
        stock = txt_stock.getText().trim();
        tipo_cmb = cmb_tipo.getSelectedIndex() + 1;

        if (costo.equals("")) {
            txt_costo.setBackground(Color.red);
            validacion++;
        }
        
        if (nombre.equals("")) {
            txt_nombre.setBackground(Color.red);
            validacion++;
        }
        if (precio.equals("")) {
            txt_precio.setBackground(Color.red);
            validacion++;
        }
        
        if (stock.equals("")) {
            txt_stock.setBackground(Color.red);
            validacion++;
        }
        if(tipo_cmb == 1){
            tipo_string = "Goma Espuma";
        } else if(tipo_cmb == 2){
            tipo_string = "Madera";
        } else if(tipo_cmb == 3){
            tipo_string = "Plastico";
        }

        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "select nombre from juguetes where nombre = '" + nombre + "'");
            ResultSet rs = pst.executeQuery();

            //Chequea que el nombre de juguete no este en uso
            if (rs.next()) {
                txt_nombre.setBackground(Color.red);
                JOptionPane.showMessageDialog(null, "Nombre de juguete ya en uso.");
                cn.close();
            } else {

                cn.close();

                if (validacion == 0) {
                    try {

                        Connection cn2 = Conexion.conectar();
                        PreparedStatement pst2 = cn2.prepareStatement(
                                "insert into juguetes values (?,?,?,?,?,?,?)");

                        pst2.setInt(1, 0);
                        pst2.setString(2, nombre);
                        pst2.setString(3, precio);
                        pst2.setString(4, tipo_string);
                        pst2.setString(5, costo);
                        pst2.setInt(6, cmb_proveedor.getSelectedIndex());
                        pst2.setString(7, stock);

                        pst2.executeUpdate();
                        cn2.close();

                        Limpiar();

                        txt_costo.setBackground(Color.green);
                        txt_nombre.setBackground(Color.green);
                        txt_precio.setBackground(Color.green);

                        JOptionPane.showMessageDialog(null, "Registro exitoso.");
                        this.dispose();

                    } catch (SQLException e) {
                        System.err.println("Error en Registrar juguete." + e);
                        JOptionPane.showMessageDialog(null, "¬°¬°ERROR al registrar!!, contacte al administrador.");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Debes de llenar todos los campos.");
                }

            }

        } catch (SQLException e) {
            System.err.println("Error en validar nombre de usario." + e);
            JOptionPane.showMessageDialog(null, "¬°¬°ERROR al comparar usuario!!, contacte al administrador.");
        }

    }//GEN-LAST:event_jButtonActionPerformed

