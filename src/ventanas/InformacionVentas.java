    private void jButton_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ActualizarActionPerformed

        int validacion = 0;
        String juguete, jardin, comprobante, fecha, cuotas;

        juguete = txt_juguete.getText().trim();
        jardin = txt_jardin.getText().trim();
        comprobante = txt_comprobante.getText().trim();
        fecha = txt_fecha.getText().trim();
        cuotas = txt_Cuotas.getText().trim();

        if (juguete.equals("")) {
            txt_jardin.setBackground(Color.red);
            validacion++;
        }
        if (jardin.equals("")) {
            txt_juguete.setBackground(Color.red);
            validacion++;
        }
        if (comprobante.equals("")) {
            txt_comprobante.setBackground(Color.red);
            validacion++;
        }
        if (fecha.equals("")) {
            txt_fecha.setBackground(Color.red);
            validacion++;
        }

        if (validacion == 0) {

            try {

                Connection cn2 = Conexion.conectar();
                PreparedStatement pst2 = cn2.prepareStatement(
                        "update ventas set  fecha=?, FacturaNro=?, cuotas=?  "
                        + "where id_ventas = '" + ID + "'");

                
                pst2.setString(1, fecha);
                pst2.setString(2, comprobante);
                pst2.setString(3, cuotas);
                

                pst2.executeUpdate();
                cn2.close();

                JOptionPane.showMessageDialog(null, "Modificación correcta.");

            } catch (SQLException e) {
                System.err.println("Error al Actualizar." + e);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Debes llenar todos los campos.");
        }

    }//GEN-LAST:event_jButton_ActualizarActionPerformed

