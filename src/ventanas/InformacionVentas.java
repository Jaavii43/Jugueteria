    public InformacionVentas() {
        initComponents();
        user = Login.user;
        venta_update = GestionarVentas.venta_update;

        setSize(610, 291);
        setResizable(false);
        setTitle("Información de las ventas a " + venta_update + " - Sesión de " + user);
        setLocationRelativeTo(null);

        setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);

        ImageIcon wallpaper = new ImageIcon("src/images/wallpaperPrincipal.jpg");
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(jLabel_Wallpaper.getWidth(),
                jLabel_Wallpaper.getHeight(), Image.SCALE_DEFAULT));
        jLabel_Wallpaper.setIcon(icono);
        this.repaint();

        jLabel_Titulo.setText("Información de la venta a:" + venta_update);

        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "select id_ventas, juguetes.nombre, jardines.Jardin, fecha, "
                            + "FacturaNro, cuotas from ventas, juguetes, jardines "
                            + "WHERE ventas.juguete_id = juguetes.id_juguetes && "
                            + "ventas.jardin_id = jardines.id_jardines && id_ventas = '" + venta_update + "'");
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                ID = rs.getInt("id_ventas");

                txt_jardin.setText(rs.getString("jardines.Jardin"));
                txt_juguete.setText(rs.getString("juguetes.nombre"));
                txt_comprobante.setText(rs.getString("FacturaNro"));
                txt_fecha.setText(rs.getString("fecha"));
                txt_Cuotas.setText(rs.getString("cuotas"));

            }
            cn.close();
        } catch (SQLException e) {
            System.err.println("Error en cargar informacion de venta. " + e);
            JOptionPane.showMessageDialog(null, "¡¡ERROR al cargar!!, contacte al administrador.");
        }

    }

