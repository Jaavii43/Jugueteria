    private void jButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActionPerformed

        int validacion = 0;
        String nombre, direccion, telefono, contacto, email;

        direccion = txt_direccion.getText().trim();
        nombre = txt_nombre.getText().trim();
        telefono = txt_telefono.getText().trim();
        contacto = txt_contacto.getText().trim();
        email = txt_email.getText().trim();

        if (direccion.equals("")) {
            txt_direccion.setBackground(Color.red);
            validacion++;
        }

        if (nombre.equals("")) {
            txt_nombre.setBackground(Color.red);
            validacion++;
        }
        if (telefono.equals("")) {
            txt_telefono.setBackground(Color.red);
            validacion++;
        }
        if (contacto.equals("")) {
            txt_contacto.setBackground(Color.red);
            validacion++;
        }
          if (email.equals("")) {
            txt_email.setBackground(Color.red);
            validacion++;
        }

        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "select Jardin from jardines where Jardin = '" + nombre + "'");
            ResultSet rs = pst.executeQuery();

            //Chequea que el nombre de juguete no este en uso
            if (rs.next()) {
                txt_nombre.setBackground(Color.red);
                JOptionPane.showMessageDialog(null, "Nombre de jardin ya en uso.");
                cn.close();
            } else {

                cn.close();

                if (validacion == 0) {
                    try {

                        Connection cn2 = Conexion.conectar();
                        PreparedStatement pst2 = cn2.prepareStatement(
                                "insert into jardines values (?,?,?,?,?,?)");

                        pst2.setInt(1, 0);
                        pst2.setString(2, nombre);
                        pst2.setString(3, direccion);
                        pst2.setString(4, contacto);
                        pst2.setString(5, telefono);
                        pst2.setString(6, email);

                        pst2.executeUpdate();
                        cn2.close();

                        Limpiar();

                        txt_direccion.setBackground(Color.green);
                        txt_nombre.setBackground(Color.green);
                        txt_telefono.setBackground(Color.green);
                        txt_contacto.setBackground(Color.green);
                        txt_email.setBackground(Color.green);

                        JOptionPane.showMessageDialog(null, "Registro exitoso.");
                        this.dispose();

                    } catch (SQLException e) {
                        System.err.println("Error en Registrar usuario." + e);
                        JOptionPane.showMessageDialog(null, "¬°¬°ERROR al registrar!!, contacte al administrador.");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Debes de llenar todos los campos.");
                }

            }

        } catch (SQLException e) {
            System.err.println("Error en validar nombre de usario." + e);
            JOptionPane.showMessageDialog(null, "¬°¬°ERROR al comparar usuario!!, contacte al administrador.");
        }

    }//GEN-LAST:event_jButtonActionPerformed

