    private void jButton_RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_RegistrarActionPerformed

        int validacion = 0, jardin_cmb, juguete_cmb, cuotas_cmb;
        String factura, string_jardin = "", string_juguete = "", string_cuotas = "";

        java.util.Date date = jDC_fecha.getDate();
        long d = date.getTime();
        java.sql.Date fecha = new java.sql.Date(d);

        factura = txt_factura.getText().trim();
        juguete_cmb = cmb_juguete_id.getSelectedIndex();
        jardin_cmb = cmb_jardin_id.getSelectedIndex();
        cuotas_cmb = cmb_Cuotas.getSelectedIndex();

        if (cuotas_cmb == 1) {
            string_cuotas = "Pago Contado";
        }
        if (cuotas_cmb == 2) {
            string_cuotas = "Cuota 1/2";
        }
        if (cuotas_cmb == 3) {
            string_cuotas = "Cuota 1/3";
        }
        if (cuotas_cmb == 4) {
            string_cuotas = "Cuota 1/4";
        }
        if (cuotas_cmb == 5) {
            string_cuotas = "Cuota 1/5";
        }
        if (cuotas_cmb == 6) {
            string_cuotas = "Cuota 1/6";
        }
        if (cuotas_cmb == 7) {
            string_cuotas = "Cuota 1/7";
        }
        if (cuotas_cmb == 8) {
            string_cuotas = "Cuota 1/8";
        }
        if (cuotas_cmb == 9) {
            string_cuotas = "Cuota 1/9";
        }
        if (cuotas_cmb == 10) {
            string_cuotas = "Cuota 1/10";
        }
        if (cuotas_cmb == 11) {
            string_cuotas = "Cuota 1/11";
        }
        if (cuotas_cmb == 12) {
            string_cuotas = "Cuota 1/12";
        }

        if (juguete_cmb != 0) {
            string_juguete = "Gusano";
        }
        if (jardin_cmb != 0) {
            string_jardin = "Cualquiera";
        }
        if (factura.equals("")) {
            txt_factura.setBackground(Color.red);
            validacion++;
        }

        if (string_jardin.equals("")) {
            cmb_jardin_id.setBackground(Color.red);
            validacion++;
        }
        if (fecha.equals("")) {
            jDC_fecha.setBackground(Color.red);
            validacion++;
        }
        if (string_juguete.equals("")) {
            cmb_juguete_id.setBackground(Color.red);
            validacion++;
        }

        if (validacion == 0) {
            try {

                Connection cn2 = Conexion.conectar();
                PreparedStatement pst2 = cn2.prepareStatement(
                        "insert into ventas values (?,?,?,?,?,?)");

                pst2.setInt(1, 0);
                pst2.setInt(2, cmb_juguete_id.getSelectedIndex());
                pst2.setInt(3, cmb_jardin_id.getSelectedIndex());
                pst2.setString(4, ((JTextField) jDC_fecha.getDateEditor().getUiComponent()).getText());
                pst2.setString(5, factura);
                pst2.setString(6, string_cuotas);

                pst2.executeUpdate();
                cn2.close();

                txt_factura.setBackground(Color.green);
                cmb_jardin_id.setBackground(Color.green);
                jDC_fecha.setBackground(Color.green);
                cmb_juguete_id.setBackground(Color.green);

                JOptionPane.showMessageDialog(null, "Registro exitoso.");
                

            } catch (SQLException e) {
                System.err.println("Error en Registrar venta." + e);
                JOptionPane.showMessageDialog(null, "¡¡ERROR al registrar!!, contacte al administrador.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debes de llenar todos los campos.");
        }

    }//GEN-LAST:event_jButton_RegistrarActionPerformed

