    public void mostrarJardin(JComboBox cmb_jardin_id) {
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("select * from jardines");
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                cmb_jardin_id.addItem(rs.getString("jardin"));
            }

        } catch (SQLException e) {
            System.err.println("Error en cargar jardines." + e);
            JOptionPane.showMessageDialog(null, "¡¡ERROR jardines!!, contacte al administrador.");
        }
    }

    public void mostrarJuguetes(JComboBox cmb_juguete_id) {
        try {
            Connection cn1 = Conexion.conectar();
            PreparedStatement pst = cn1.prepareStatement("select * from juguetes");
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                cmb_juguete_id.addItem(rs.getString("nombre"));
            }

        } catch (SQLException e) {
            System.err.println("Error en cargar juguetes." + e);
            JOptionPane.showMessageDialog(null, "¡¡ERROR juguetess!!, contacte al administrador.");
        }
    }

